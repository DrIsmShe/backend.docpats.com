// S.7.7+ обновления:
//   • LandmarkSchema.mediapipeIndex — расширен с 0..467 до 0..477 (478 точек)
//   • LandmarkSchema.group — расширен enum: forehead, chin, cheeks, jaws, manual, ears
//   • landmarks array length — теперь принимает 0, 6 (manual_fit), 468, 478
//   • LandmarksMetaSchema — добавлены: source, facesDetected, selectedFaceIndex,
//     rotationUsed (для multi-face и rotation retry)
//   • LandmarksMetaSchema.pointsCount — расширен до 478

import mongoose from "mongoose";

const { Schema } = mongoose;

/* ──────────────────────────────────────────────────────────────────────────
   Control point (без изменений)
   ────────────────────────────────────────────────────────────────────────── */
const ControlPointSchema = new Schema(
  {
    key: { type: String, required: true },
    anchor: {
      x: { type: Number, required: true, min: 0, max: 1 },
      y: { type: Number, required: true, min: 0, max: 1 },
    },
    current: {
      x: { type: Number, required: true, min: 0, max: 1 },
      y: { type: Number, required: true, min: 0, max: 1 },
    },
    radius: { type: Number, required: true, min: 0.001, max: 1 },
    strength: { type: Number, required: true, min: -1, max: 1 },
  },
  { _id: false },
);

/* ──────────────────────────────────────────────────────────────────────────
   Anatomical landmark (S.7) — точка из MediaPipe или ручной разметки.

   id              — стабильный идентификатор 'lm_<index>' или 'manual_<...>'
   mediapipeIndex  — 0..477 для MediaPipe, -1 для manual landmarks
   group           — анатомическая группа (расширенный enum)
   x, y            — нормализованные [0..1]
   z               — глубина (может быть отрицательной)
   autoGenerated   — true для авто-точек, false для ручных (manual group)

   _id: false — экономия 9KB на план
   ────────────────────────────────────────────────────────────────────────── */
const LandmarkSchema = new Schema(
  {
    id: { type: String, required: true },
    // S.7.7+: было 0..467 (468 точек). Сейчас 478 точек включая iris (10)
    // и -1 для manual landmarks (group: 'manual')
    mediapipeIndex: { type: Number, required: true, min: -1, max: 477 },
    group: {
      type: String,
      required: true,
      enum: [
        // Базовые из faceLandmarksGroups.js
        "face_oval",
        "forehead",
        "nose",
        "lips",
        "left_eye",
        "right_eye",
        "left_eyebrow",
        "right_eyebrow",
        // S.7.5+ расширенные группы
        "left_cheek",
        "right_cheek",
        "left_jaw",
        "right_jaw",
        "chin",
        "ears",
        // S.7.5 — manual landmarks (autoGenerated:false)
        "manual",
        // Fallback
        "other",
      ],
    },
    x: { type: Number, required: true, min: 0, max: 1 },
    y: { type: Number, required: true, min: 0, max: 1 },
    z: { type: Number, default: 0 },
    autoGenerated: { type: Boolean, default: true },
    // S.7.7+ — для manual_fit landmarks отмечаем источник
    source: { type: String, default: null, maxlength: 32 },
    hidden: { type: Boolean, default: false },
  },
  { _id: false },
);

/* ──────────────────────────────────────────────────────────────────────────
   Метаданные о последней детекции landmarks.

   detectedAt        — таймстамп
   modelVersion      — 'mediapipe@0.10.18' или 'manual_fit@v1'
   imageWidth/H      — пиксельный размер фото
   pointsCount       — 0, 6, 468 или 478
   source            — 'auto' | 'manual_fit' (S.7.7+)
   facesDetected     — кол-во лиц найденных MediaPipe (S.7.7 multi-face)
   selectedFaceIndex — какое из них выбрано
   rotationUsed      — 0/90/180/270 если сработал rotation retry
   anchorCount       — для manual_fit: сколько точек кликнул врач
   ────────────────────────────────────────────────────────────────────────── */
const LandmarksMetaSchema = new Schema(
  {
    detectedAt: { type: Date, default: null },
    modelVersion: { type: String, default: null, maxlength: 64 },
    imageWidth: { type: Number, default: null, min: 1, max: 20000 },
    imageHeight: { type: Number, default: null, min: 1, max: 20000 },
    // S.7.7+: пересчитан max с 468 на 478
    pointsCount: { type: Number, default: 0, min: 0, max: 478 },

    // S.7.7+ — multi-face и rotation retry
    source: {
      type: String,
      default: "auto",
      enum: ["auto", "manual_fit"],
    },
    facesDetected: { type: Number, default: 0, min: 0, max: 10 },
    selectedFaceIndex: { type: Number, default: 0, min: 0, max: 9 },
    rotationUsed: {
      type: Number,
      default: 0,
      enum: [0, 90, 180, 270],
    },
    anchorCount: { type: Number, default: 0, min: 0, max: 20 },
  },
  { _id: false },
);

/* ──────────────────────────────────────────────────────────────────────────
   Embedded photo (без изменений)
   ────────────────────────────────────────────────────────────────────────── */
const PhotoSchema = new Schema(
  {
    r2Key: { type: String, required: true },
    url: { type: String, required: true },
    width: { type: Number, required: true, min: 1 },
    height: { type: Number, required: true, min: 1 },
    size: { type: Number, required: true, min: 0 },
    mimeType: { type: String, required: true },
    uploadedAt: { type: Date, required: true, default: Date.now },
  },
  { _id: false },
);

/* ──────────────────────────────────────────────────────────────────────────
   Допустимые длины массива landmarks:
     • 0       — детекция ещё не запускалась / провалилась
     • 6       — manual_fit (6-точечная разметка S.7.7+)
     • 468     — старые auto-detection (legacy данные в БД)
     • 478     — текущая auto-detection (с iris)
   ────────────────────────────────────────────────────────────────────────── */
const ALLOWED_LANDMARK_LENGTHS = new Set([0, 6, 468, 478]);

const SimulationPlanSchema = new Schema(
  {
    doctorId: {
      type: Schema.Types.ObjectId,
      required: true,
      index: true,
    },

    labelEncrypted: { type: String, required: true },
    patientRefEncrypted: { type: String, default: null },

    photo: { type: PhotoSchema, required: true },
    controlPoints: { type: [ControlPointSchema], default: [] },

    // S.7.7+ — расширенная валидация длины
    landmarks: {
      type: [LandmarkSchema],
      default: [],
      validate: {
        validator: (arr) => ALLOWED_LANDMARK_LENGTHS.has(arr.length),
        message:
          "landmarks must be 0 (none), 6 (manual_fit), 468 (legacy auto), or 478 (auto)",
      },
    },
    landmarksMeta: {
      type: LandmarksMetaSchema,
      default: () => ({}),
    },

    deletedAt: { type: Date, default: null },
  },
  {
    timestamps: true,
    collection: "simulation_plans",
  },
);

SimulationPlanSchema.index({ doctorId: 1, createdAt: -1 });
SimulationPlanSchema.index({ doctorId: 1, deletedAt: 1 });

const SimulationPlan = mongoose.model("SimulationPlan", SimulationPlanSchema);

export default SimulationPlan;

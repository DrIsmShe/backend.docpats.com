// modules/clinic/clinic-staff/routes/staff.routes.js
//
// REST endpoints for ClinicMembership management (staff = clinic members).
//
// Mount path (from parent clinic router): /api/v1/clinic/*
// Final URLs:
//   GET    /api/v1/clinic/staff/search-doctors
//   GET    /api/v1/clinic/staff
//   POST   /api/v1/clinic/staff
//   PATCH  /api/v1/clinic/staff/:id/role
//   DELETE /api/v1/clinic/staff/:id
//
// AUDIT MIDDLEWARE (HIPAA §164.312(b)):
//   Every endpoint is audited to hipaa_audit_logs. Staff actions are
//   PHI-adjacent (membership data + decrypted PII names/emails of
//   medical staff) — auditing these is required for compliance.
//
//   PHI safety rule for metaFrom: never log decrypted names, emails,
//   or phone numbers. Safe to log: role names (business permission,
//   not PHI), counts, structural flags.

import express from "express";
import * as ctrl from "../controllers/staff.controller.js";
import auditMiddleware from "../../../audit/middleware/auditMiddleware.js";

const router = express.Router();

// ─── Doctor search ────────────────────────────────────────────────────
// Used by the "Add staff" flow to find existing DocPats users to
// invite as clinic staff. Returns doctor profiles (public/professional
// info, not PHI of patients).
//
// MUST come before /staff to avoid /:id collision in any nested router.
router.get(
  "/staff/search-doctors",
  auditMiddleware({
    resourceType: "clinic-employee",
    action: "clinic.staff.list",
    metaFrom: (req) => ({
      // What kind of query was used — but not the value itself.
      // Doctor names are professional info (not PHI), but search
      // strings can contain typos / partial PII so safer to omit.
      searchedBy: req.query?.email
        ? "email"
        : req.query?.name
          ? "name"
          : req.query?.specialization
            ? "specialization"
            : "unknown",
      limit: Number(req.query?.limit) || 10,
    }),
  }),
  ctrl.searchDoctors,
);

// ─── List staff ───────────────────────────────────────────────────────
// Returns all clinic members with decrypted PII (names, emails of staff).
// This is a HIPAA-loggable event — auditor needs to know who pulled
// the full staff list.
router.get(
  "/staff",
  auditMiddleware({
    resourceType: "clinic-employee",
    action: "clinic.staff.list",
    metaFrom: (req) => ({
      limit: Number(req.query?.limit) || 50,
      hasFilter: Boolean(req.query?.role || req.query?.status),
      roleFilter: req.query?.role || null,
    }),
  }),
  ctrl.listStaff,
);

// ─── Add staff member ─────────────────────────────────────────────────
// Adds an existing DocPats user (or creates a ClinicEmployee record)
// to this clinic with a specified role. Critical event — grants access
// to clinic-level PHI for the new staff member.
//
// resourceOwnerIdFrom captures WHO is being added (the new staff's
// User ID, if applicable). The new ClinicMembership ID isn't known
// at middleware level (generated by service after response).
router.post(
  "/staff",
  auditMiddleware({
    resourceType: "clinic-employee",
    action: "clinic.staff.add",
    resourceOwnerIdFrom: (req) => req.body?.userId,
    metaFrom: (req) => ({
      // Role is business permission, not PHI — safe to log.
      role: req.body?.role || null,
      // Whether this was a User link or a new ClinicEmployee.
      actorType: req.body?.userId ? "user" : "employee",
    }),
  }),
  ctrl.addStaff,
);

// ─── Change role ──────────────────────────────────────────────────────
// Privilege escalation/de-escalation — critical for security audit.
// "Who promoted X to admin?" is a textbook HIPAA forensics query.
router.patch(
  "/staff/:id/role",
  auditMiddleware({
    resourceType: "clinic-employee",
    action: "clinic.staff.role_change",
    resourceIdFrom: "params.id",
    metaFrom: (req) => ({
      newRole: req.body?.role || null,
    }),
  }),
  ctrl.updateStaffRole,
);

// ─── Remove staff ─────────────────────────────────────────────────────
// Soft-remove. The membership record stays in DB (isRemoved: true)
// for audit/compliance reasons — never hard-delete staff records.
router.delete(
  "/staff/:id",
  auditMiddleware({
    resourceType: "clinic-employee",
    action: "clinic.staff.remove",
    resourceIdFrom: "params.id",
  }),
  ctrl.removeStaff,
);

export default router;

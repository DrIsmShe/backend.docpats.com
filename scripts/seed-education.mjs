async function main() {
  await mongoose.connect(MONGODB_URI, { dbName: DB_NAME });
  console.log(`✅ Подключено к ${DB_NAME}\n`);

  const ExamProgram = (
    await import("../modules/education/education-catalog/models/examProgram.model.js")
  ).default;
  const ExamItem = (
    await import("../modules/education/education-items/models/examItem.model.js")
  ).default;
  const ExamAttempt = (
    await import("../modules/education/education-attempts/models/examAttempt.model.js")
  ).default;
  const User = (await import("../common/models/Auth/users.js")).default;
  const itemService = await import(
    "../modules/education/education-items/services/item.service.js"
  );

  // ─── Удаление демо-данных ───
  if (WIPE || REMOVE_ONLY) {
    const existing = await ExamProgram.findOne({ code: PROGRAM_CODE }).lean();
    if (existing) {
      const items = await ExamItem.deleteMany({ programId: existing._id });
      const attempts = await ExamAttempt.deleteMany({ programId: existing._id });
      await ExamProgram.deleteOne({ _id: existing._id });
      console.log(
        `🗑  Удалено: программа "${existing.code}", вопросов ${items.deletedCount}, попыток ${attempts.deletedCount}\n`,
      );
    } else {
      console.log("🗑  Демо-программа не найдена, удалять нечего\n");
    }
    if (REMOVE_ONLY) {
      await mongoose.disconnect();
      return;
    }
  }

  // ─── Защита от повторного запуска ───
  const duplicate = await ExamProgram.findOne({ code: PROGRAM_CODE }).lean();
  if (duplicate) {
    console.log(
      `⚠️  Программа "${PROGRAM_CODE}" уже существует (id: ${duplicate._id}).`,
    );
    console.log("    Перезалить: node scripts/seed-education.mjs --wipe\n");
    await mongoose.disconnect();
    return;
  }

  // ─── Рецензент ───
  // Вопрос не может быть опубликован без человека-рецензента — это гейт
  // модуля, и скрипт его не обходит. Берём реального админа, если он есть.
  const admin = await User.findOne({ role: "admin" }).select("_id email").lean();
  const reviewerId = admin?._id ?? new mongoose.Types.ObjectId();
  if (admin) {
    console.log(`👤 Рецензент: ${admin.email}`);
  } else {
    console.log(
      "👤 Пользователь с ролью admin не найден — рецензент проставлен техническим id",
    );
  }

  // ─── Программа ───
  const program = await ExamProgram.create({
    code: PROGRAM_CODE,
    title: "Демо: терапия, базовый уровень",
    description:
      "Демонстрационная программа для проверки модуля подготовки. Учебные вопросы по кардиологии, неврологии и эндокринологии. Не является материалом реального экзамена.",
    country: "INT",
    region: "international",
    examType: "cme",
    authority: "DocPats (демо)",
    languages: ["ru"],
    blueprint: BLUEPRINT,
    defaultQuestionCount: 10,
    defaultDurationMinutes: 20,
    passingScorePercent: 60,
    sourcePolicy: "original",
    isFree: true,
    status: "published",
    publishedAt: new Date(),
    createdBy: reviewerId,
  });
  console.log(`\n📚 Программа создана: ${program.title}`);
  console.log(`   id: ${program._id}`);
  console.log(
    `   темы: ${BLUEPRINT.map((b) => `${b.title} ${b.weightPercent}%`).join(", ")}\n`,
  );

  // ─── Вопросы через настоящий редакторский цикл ───
  let published = 0;
  const failures = [];

  for (const [i, q] of QUESTIONS.entries()) {
    try {
      const draft = await itemService.createItem({
        programId: program._id,
        topicCode: q.topicCode,
        lang: "ru",
        type: "sba",
        stem: q.stem,
        options: q.options,
        correctKeys: q.correctKeys,
        explanation: q.explanation,
        references: q.references ?? [],
        difficulty: q.difficulty,
        source: { kind: "original", authority: "DocPats (демо)" },
        actorId: reviewerId,
      });

      await itemService.submitForReview(draft._id, reviewerId);
      await itemService.reviewItem(draft._id, {
        decision: "approve",
        reviewerId,
      });
      published += 1;
      process.stdout.write(`   ✅ ${i + 1}/${QUESTIONS.length}\r`);
    } catch (err) {
      failures.push({ index: i + 1, stem: q.stem.slice(0, 60), reason: err.message });
    }
  }

  console.log(`\n✅ Опубликовано вопросов: ${published}/${QUESTIONS.length}`);

  if (failures.length) {
    console.log("\n❌ Не прошли гейт публикации:");
    for (const f of failures) {
      console.log(`   #${f.index} «${f.stem}…» → ${f.reason}`);
    }
  }

  const byTopic = await ExamItem.aggregate([
    { $match: { programId: program._id, status: "published" } },
    { $group: { _id: "$topicCode", count: { $sum: 1 } } },
    { $sort: { _id: 1 } },
  ]);
  console.log("\n📊 По темам:");
  for (const row of byTopic) {
    const title = BLUEPRINT.find((b) => b.code === row._id)?.title ?? row._id;
    console.log(`   ${title}: ${row.count}`);
  }

  console.log("\n───────────────────────────────────────────────");
  console.log("Открыть в браузере:  /education");
  console.log(`API программы:       GET /api/v1/education/programs/${program._id}`);
  console.log("───────────────────────────────────────────────\n");

  await mongoose.disconnect();
}

main().catch(async (err) => {
  console.error("❌ Ошибка:", err);
  await mongoose.disconnect().catch(() => {});
  process.exit(1);
});
